# Solicita uma string do usuário
string_usuario = input("Por favor, insira uma string:")

# Solicita um número inteiro do usuário
numero_usuario = int(input("Por favor, insira um número inteiro: "))

# Repete a string o número de vezes informado
resultado = (string_usuario + ' ') * numero_usuario

# Exibe o resultado
print("A string repetida é: ", resultado)
